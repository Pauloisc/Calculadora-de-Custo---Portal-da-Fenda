public class ConeDeLuz {
    String nome;
    double custoBase;

    public ConeDeLuz(String nome, double custoBase) {
        this.nome = nome;
        this.custoBase = custoBase;
    }

    public String getNome() {
        return nome;
    }

    public void setNome(String nome) {
        this.nome = nome;
    }

    public double getCustoBase() {
        return custoBase;
    }

    public void setCustoBase(double custoBase) {
        this.custoBase = custoBase;
    }
}
